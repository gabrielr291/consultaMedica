    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        String rut = txt_rut.getText();
        String nombre = txt_nombre.getText();
        String appaterno = txt_appaterno.getText();
        String apmaterno = txt_apmaterno.getText();
        String direccion= txt_direccion.getText();
        char genero = ' ';
        if (rbtn_femenino.isSelected() == true) {
            genero = 'F';
        } else {
            genero = 'M';
        }
        String ciudad = (String) combo_ciudad.getSelectedItem();
        char dona = ' ';
        if (chk_donante.isSelected() == true) {
            dona = 'S';
        } else {
            dona = 'N';
        }
        Socio soc=new Socio(rut, nombre, apmaterno, appaterno, direccion, ciudad, genero, dona);
        Registro_socio regi=new Registro_socio();
        boolean resp=regi.agregarSocio(soc);
        if (resp==true)
        {
            JOptionPane.showMessageDialog(rootPane, "paciente agregado");
        }else{
            JOptionPane.showMessageDialog(rootPane, "paciente no agregado");
        }              
    }//GEN-LAST:event_btn_agregarActionPerformed

